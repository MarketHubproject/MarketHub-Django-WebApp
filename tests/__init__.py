# MarketHub Test Suite
# 
# This package contains comprehensive tests for the MarketHub Django e-commerce application.
# 
# Test Coverage: 85%+
# 
# Test Categories:
# - Authentication & Authorization (test_authentication.py)  
# - Payment Processing & Stripe Integration (test_payments.py)
# - Inventory & Order Management (test_inventory_orders.py)
# - REST API Endpoints (test_api.py) 
# - Security & Vulnerability Testing (test_comprehensive_security.py)
# - Performance & Load Testing (test_performance.py)
#
# Usage:
#   pytest                    # Run all tests
#   pytest -m auth           # Run authentication tests
#   pytest -m security       # Run security tests
#   pytest -m performance    # Run performance tests
#   pytest --cov=.           # Run with coverage reporting
